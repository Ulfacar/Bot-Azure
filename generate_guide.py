def warn(text):
    p = doc.add_paragraph()
    run = p.add_run("⚠️ " + text)
    run.bold = True
    run.font.color.rgb = RGBColor(0xCC, 0x00, 0x00)
    return p


def bold_line(bold_part, rest):
    p = doc.add_paragraph()
    run = p.add_run(bold_part)
    run.bold = True
    p.add_run(rest)
    return p


